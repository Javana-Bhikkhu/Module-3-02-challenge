function switchOn() {
    var light = document.getElementById("light");
    light.style.backgroundImage = "url('./img/switch-on-png')"; // switch on
  }
  
  function switchOff() {
    var light = document.getElementById("light");
    light.style.backgroundImage = "url('./img/switch-off.png')"; // switch off
  };

  function switchOn() {
    var light = document.getElementById("light");
    light.style.backgroundColor = "yellow"; // switch on
  }
  
  function switchOff() {
    var light = document.getElementById("light");
    light.style.backgroundColor = "grey"; // switch off
  }
  
  